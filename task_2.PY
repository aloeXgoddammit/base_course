b1 = int(input("Введите первый член геометрической прогрессии: "))
q = int(input("Введите знаменатель геометрической прогрессии: "))
n = int(input("Введите кол-вол чисел геометрической прогрессии: "))

for i in range(n):
    geo_prog = b1 * (q ** i)
    print(f"Член прогрессии №{i + 1}: {geo_prog}")
